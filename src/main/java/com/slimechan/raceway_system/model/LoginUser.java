package com.slimechan.raceway_system.model;

public class LoginUser{

    public String login;
    public String pass;

    public void setLogin(String login){
        this.login = login;
    }
    public String getLogin(){
        return login;
    }

    public void setPass(String pass){
        this.pass = pass;
    }
    public String getPass(){
        return this.pass;
    }
}